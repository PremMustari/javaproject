public class PrimitivetypeExample {
    public static void main(String[] args){
        //Integer types
        byte byteVar = 127; //8-bit
        short shortVar = 32767; //16-bit
        int intVar = 2147483647; //32-bit
        long longVar = 922337203; //64-bit


        
        //Floating point types 
        float floatVar = 3.14159265f; //32-bit
        double doubleVar = 3.141592653589793; //64-bit

        //Character type
        char charVar = 'A';//16-bit

        //Boolean type
        boolean booleanVar = true; //represents true or false

        //ouutput
        System.out.println("Byte Var :"+byteVar);
        System.out.println("Short Var :"+shortVar);
        System.out.println("Int Var :"+intVar);
        System.out.println("Long Var :"+longVar);
        System.out.println("Float Var :"+floatVar);
        System.out.println("Double Var :"+doubleVar);
        System.out.println("Character Var :"+charVar);
        System.out.println("Boolean Var :"+booleanVar);
    }
}
